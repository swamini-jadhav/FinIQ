def get_chatbot_response(message, context=None):
    """Generate chatbot response based on user message"""
    try:
        message_lower = message.lower()
        
        # Find matching category
        matched_category = 'default'
        for category, keywords in KEYWORDS.items():
            if any(keyword in message_lower for keyword in keywords):
                matched_category = category
                break
        
        # Get response
        responses = RESPONSES.get(matched_category, RESPONSES['default'])
        response_text = random.choice(responses)
        
        # Add context-specific information if available
        if context and 'ticker' in context:
            ticker = context['ticker']
            if 'stock' in message_lower or 'ticker' in message_lower:
                response_text += f"\n\nI see you're interested in {ticker}. Use the search function to get detailed analysis!"
        
        return {
            'response': response_text,
            'category': matched_category,
            'suggestions': get_suggestions(matched_category)
        }
        
    except Exception as e:
        return {
            'response': "I apologize, but I encountered an error processing your message. Please try rephrasing your question.",
            'category': 'error',
            'suggestions': [
                "How do predictions work?",
                "What stocks are supported?",
                "How to get started?"
            ]
        }


